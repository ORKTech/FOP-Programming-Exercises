//Library Included:
#include <stdio.h>

//Main:
int main()
{
    int num, i;

    // Input from user
    printf("Enter a number to generate its multiplication table: ");
    scan_f("%d", &num);

    // Generate multiplication table
    printf("\nMultiplication Table of %d:\n", num);

    for (i = 1; i <= 10; i++)
    {
        printf("%d x %d = %d\n", num, i, num * i);
    }

    return 0;
}